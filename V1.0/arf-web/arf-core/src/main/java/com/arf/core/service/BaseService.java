package com.arf.core.service;

import java.io.Serializable;
import java.util.List;

import com.arf.base.AppLog;
import com.arf.core.Filter;
import com.arf.core.Order;
import com.arf.core.Page;
import com.arf.core.Pageable;
import com.arf.core.entity.BaseEntity;

/**
 * Service - 基类
 * 
 * @author arf
 * @version 4.0
 */
public interface BaseService<T extends BaseEntity<ID>, ID extends Serializable> {
	public final String DEFAULT_DATE_FORMATTER = "yyyy-MM-dd HH:mm:ss";
	/**
	 * 查找实体对象
	 * 
	 * @param id
	 *            ID
	 * @return 实体对象，若不存在则返回null
	 */
	T find(ID id);

	/**
	 * 查找所有实体对象集合
	 * 
	 * @return 所有实体对象集合
	 */
	List<T> findAll();

	/**
	 * 查找实体对象集合
	 * 
	 * @param ids
	 *            ID
	 * @return 实体对象集合
	 */
	List<T> findList(ID... ids);

	/**
	 * 查找实体对象集合
	 * 
	 * @param count
	 *            数量
	 * @param filters
	 *            筛选
	 * @param orders
	 *            排序
	 * @return 实体对象集合
	 */
	List<T> findList(Integer count, List<Filter> filters, List<Order> orders);

	/**
	 * 查找实体对象集合
	 * 
	 * @param first
	 *            起始记录
	 * @param count
	 *            数量
	 * @param filters
	 *            筛选
	 * @param orders
	 *            排序
	 * @return 实体对象集合
	 */
	List<T> findList(Integer first, Integer count, List<Filter> filters, List<Order> orders);

	/**
	 * 查找实体对象分页
	 * 
	 * @param pageable
	 *            分页信息
	 * @return 实体对象分页
	 */
	Page<T> findPage(Pageable pageable);

	/**
	 * 查询实体对象总数
	 * 
	 * @return 实体对象总数
	 */
	long count();

	/**
	 * 查询实体对象数量
	 * 
	 * @param filters
	 *            筛选
	 * @return 实体对象数量
	 */
	long count(Filter... filters);

	/**
	 * 判断实体对象是否存在
	 * 
	 * @param id
	 *            ID
	 * @return 实体对象是否存在
	 */
	boolean exists(ID id);

	/**
	 * 判断实体对象是否存在
	 * 
	 * @param filters
	 *            筛选
	 * @return 实体对象是否存在
	 */
	boolean exists(Filter... filters);

	/**
	 * 保存实体对象
	 * 
	 * @param entity
	 *            实体对象
	 * @return 实体对象
	 */
	T save(T entity);

	/**
	 * 更新实体对象
	 * 
	 * @param entity
	 *            实体对象
	 * @return 实体对象
	 */
	T update(T entity);

	/**
	 * 更新实体对象
	 * 
	 * @param entity
	 *            实体对象
	 * @param ignoreProperties
	 *            忽略属性
	 * @return 实体对象
	 */
	T update(T entity, String... ignoreProperties);

	/**
	 * 删除实体对象
	 * 
	 * @param id
	 *            ID
	 */
	void delete(ID id);

	/**
	 * 删除实体对象
	 * 
	 * @param ids
	 *            ID
	 */
	void delete(ID... ids);

	/**
	 * 删除实体对象
	 * 
	 * @param entity
	 *            实体对象
	 */
	void delete(T entity);
	/**
	 * 保存app操作日志 已废弃
	 * @see {@link com.arf.core.service.BaseService.saveAppLogs(AppLog)}
	 * @param tablename
	 * @param community_number
	 * @param username
	 * @param type
	 * @param licenseplate
	 * @param descript
	 * @param version
	 * @param deviceType
	 * @param deviceNumber
	 */
	@Deprecated
	void saveAppLogs(String tablename,String community_number,String username,AppLog.LogType type,String licenseplate,String descript,
			String version,String deviceType,String deviceNumber);
	/**
	 *  保存app操作日志,内部实现已经做过try_caych,直接调用该方法即可无需做异常处理
	 * @param appLog applog_yyyyMM表的实体
	 */
	void saveAppLogs(AppLog appLog);
	
	/**
	 * 判断表是不是存在
	 * @param tableName
	 * @return
	 */
	boolean existTable(String tableName);
	
	/**
	 * 复制sourceTable的表结构到新的表newTable
	 * @param sourceTable
	 * @param newTable
	 * @return
	 */
	boolean copyTable(String sourceTable,String newTable);
}
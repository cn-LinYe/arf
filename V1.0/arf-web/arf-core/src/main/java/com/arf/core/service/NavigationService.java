package com.arf.core.service;

import java.util.List;

import com.arf.core.Filter;
import com.arf.core.Order;
import com.arf.core.entity.Navigation;
import com.arf.core.entity.Navigation.Position;

/**
 * Service - 导航
 * 
 * @author arf
 * @version 4.0
 */
public interface NavigationService extends BaseService<Navigation, Long> {

	/**
	 * 查找导航
	 * 
	 * @param position
	 *            位置
	 * @return 导航
	 */
	List<Navigation> findList(Position position);

	/**
	 * 查找导航
	 * 
	 * @param count
	 *            数量
	 * @param filters
	 *            筛选
	 * @param orders
	 *            排序
	 * @param useCache
	 *            是否使用缓存
	 * @return 导航
	 */
	List<Navigation> findList(Integer count, List<Filter> filters, List<Order> orders, boolean useCache);

}